  static String getRubaState(int rubNum) {
    var result = ((rubNum-1)/4.0);
    String rubName="";
    if(result==0.0){
      rubName ="";
    }else if(result==0.25){
      rubName ="ربع";
    }else if(result==0.5){
      rubName ="نصف";
    }else if(result==0.75){
      rubName ="تلات ارباع";
    }
    return rubName;
  }


