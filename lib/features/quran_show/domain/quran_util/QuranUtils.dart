  static AyaNumPositions getAyaNumOfPosition(int x, int y, List<AyaNumPositions> ayaNumPositions) {
    var ayaNum = null ;
    for (final ayaNumPosition in ayaNumPositions) {
      Rect rect = Rect.fromLTRB(
        (ayaNumPosition.X?.toDouble()??0),
        (ayaNumPosition.Y?.toDouble()??0),
        (ayaNumPosition.XMax?.toDouble()??0),
        (ayaNumPosition.YMax?.toDouble()??0),
      );
      var found = rect.isInside(x, y);
      if (found)
        return ayaNumPosition ;
    }
    return ayaNum ;
  }

  static List<Widget> getHighlightedAyatListViews(double scale ,double scaleRatio ,List<ExportLine>? ayaRectsMap,BuildContext context) {
        List<Widget> list = [];
        if (ayaRectsMap != null) {
          List<Rect>? ayaRects = [];
          for(ExportLine line in ayaRectsMap){
            ayaRects.add(Rect.fromLTRB(
                ((line.X.toDouble()/scale)/scaleRatio),0,((line.XMax.toDouble()/scale)/scaleRatio),((line.YMax.toDouble()/scale)/scaleRatio)
            ));//.applyScale(scale)
          }

          if (ayaRects != null) {
            for (final rect in ayaRects) {
              Widget widget = Positioned(
                  left: rect.left,
                  top: rect.top,
                  child: Container(
                    color: context.isNightMode()?  ColorManager.HighlightNightAyatColor.withAlpha(130) : ColorManager.HighlightAyatColor.withAlpha(120),
                    width: rect.width.isNaN? 0: rect.width.abs(),
                    height: rect.height.isNaN? 0: rect.height.abs(),
                  ));
              list.add(widget);
            }
          }
        }
      return list ;
  }

